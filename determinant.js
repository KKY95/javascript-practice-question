function deter(A){
    let n=A.length;
    let d=0;
    if(n==2)
    {
        d=(A[0][0]*A[1][1])-(A[1][0]*A[0][1]);
    }
    else if(n==3)
    {
        d=(A[0][0]*((A[1][1]*A[2][2])-(A[2][1]*A[1][2])))
         -(A[0][1]*((A[1][0]*A[2][2])-(A[2][0]*A[1][2])))
         +(A[0][2]*((A[1][0]*A[2][1])-(A[2][0]*A[1][1])));
    }
    return d;
}

